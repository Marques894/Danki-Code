# int = Inteiros, números sem casas decimais, como 1, 0, -5, etc.

# float = Pontos flutuantes, números com casas decimais, como 1.5, -2.7, 3.0, etc.

# complex = Números complexos, com parte real e parte imaginária, representados como a + bj, como 3 + 2j.

# bool = Tipo booleano, representa valores lógicos True (verdadeiro) ou False (falso).

# str = Strings, sequências de caracteres, usadas para textos, como "Olá, mundo!".

# list = Listas, sequências ordenadas e mutáveis, que podem conter tipos variados, como [1, 2.5, "texto", True].

# tuple = Tuplas, semelhantes a listas, mas imutáveis, como (1, 2.5, "texto").

# dict = Dicionários, coleções de pares chave-valor, como {"nome": "João", "idade": 30}.

# set = Conjuntos, coleções de elementos únicos e não ordenados, como {1, 2, 3}.






